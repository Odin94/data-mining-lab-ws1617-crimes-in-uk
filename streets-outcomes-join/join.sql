insert into crimes_uk_crimes (
  crime_id,
  month,
  reported_by,
  falls_within,
  longitude,
  latitude,
  location,
  lsoa_code,
  lsoa_name,
  crime_type,
  last_outcome_category,
  suspect_identified,
  context,
  county,
  outcome_type,
  suspect_identified_outcome)
select
  s.crime_id,
  s.month,
  s.reported_by,
  s.falls_within,
  s.longitude,
  s.latitude,
  s.location,
  s.lsoa_code,
  s.lsoa_name,
  s.crime_type,
  s.last_outcome_category,
  s.suspect_identified,
  s.context,
  s.county,
  o.outcome_type,
  o.suspect_identified
from crimes_uk_streets s left outer join crimes_uk_outcomes o on s.crime_id = o.crime_id;
